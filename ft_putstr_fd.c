#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	aux;

	aux = 0;
	if (!s)
	{
		return ;
	}
	while (s[aux] != '\0')
	{
		write(fd, &s[aux], 1);
		aux++;
	}
}
