#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	aux;

	if (!s)
	{
		return ;
	}
	aux = 0;
	while (s[aux] != '\0')
	{
		write(fd, &s[aux], 1);
		aux++;
	}
	write(fd, "\n", 1);
}
